# Comparison operators are symbols we use in Python to compare two values

# One "equal to" symbol means assignment
# Doube "equal to" symbol is for comparison

# Equal to (==)
print("Is 5 equal to 5?", 5 == 5)

# Not Equal To
print("Is 5 not equal to 5?", 5 != 5)

# Greater Than (>)
print("Is 7 greater than 10?", 7 > 10)

# Less Than (<)
print("Is 7 less than 10?", 7 < 10)

# Greater Than or Equal To (>=)
print("Is 7 greater than or equal to 10?", 7 >= 10)

# Less Than or Equal To (<=)
print("Is 7 less than or equal to 10?", 7 <= 10)